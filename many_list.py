def color(colors):
    if colors == "red":
        colors = "#ff0000"
    if colors == "green":
        colors = "#008000"
    if colors == "yellow":
        colors = "#ffff00"
    if colors == "orange":
        colors = "#ffa500"
    if colors == "gold":
        colors = "#ffd700"
    if colors == "black":
        colors = "#000000"
    if colors =="white" or colors=="snow":
        colors = "#ffffff"
    if colors == "blue":
        colors = "#0000ff"
    if colors == "skyblue":
        colors = "#87ceeb"
    if colors == "lime":
        colors = "#00ff00"
    if colors == "purple":
        colors = "#800080"
    if colors == "pink":
        colors = "#ffc0cb"
    return colors